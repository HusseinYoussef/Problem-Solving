double rec(int num, int tm)        // probability   enter or not
{
	if (tm > t || num == n) return num;

	double &ret = dp[num][tm];
	if (ret != -1) return ret;

	return ret = rec(num + 1, tm + 1)*p + rec(num, tm + 1)*(1 - p);
}

